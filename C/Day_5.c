//Relational operator or comparative operator 
#include <stdio.h>
int main()
{
int num_1, num_2;

printf("num_1 > num_2: %d \n", num_1 > num_2);
printf("num_1 >= num_2: %d \n", num_1 >= num_2);
printf("num_1 <= num_2: %d \n", num_1 <= num_2);
printf("num_1 < num_2: %d \n", num_1 < num_2);
printf("num_1 == num_2: %d \n", num_1 == num_2);
printf("num_1 != num_2: %d \n", num_1 != num_2);

return 0;
}
